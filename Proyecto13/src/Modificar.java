    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        String id= txtNumEmp.getText();
        String Nombre= txtNombre.getText();
        String Apellidos= txtApellidos.getText();
        String Direccion= txtDireccion.getText();
        
        Empleados emp= new Empleados (id,Nombre,Apellidos,Direccion);
        contenedor.set(buscar, emp);
        System.exit(0);
    }//GEN-LAST:event_BtnModificarActionPerformed

    private void txtNumEmpKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumEmpKeyTyped
        int press = evt.getKeyChar();
        if(press == 13){
            String idencontrado=txtNumEmp.getText().trim();
            Empleados emp;
            for(int i = 0; i < contenedor.size(); i++){
                 
                emp= (Empleados) contenedor.get(i);
                
                if (idencontrado.equalsIgnoreCase(emp.getId())){
                    txtRFC.setText(emp.getRfc());
                    txtNombre.setText(emp.getNombre());
                    txtApellidos.setText(emp.getApellidos());
                    txtDireccion.setText(emp.getDireccion());
                    buscar=i;
                    break;
                }
            }
        }
    }//GEN-LAST:event_txtNumEmpKeyTyped


